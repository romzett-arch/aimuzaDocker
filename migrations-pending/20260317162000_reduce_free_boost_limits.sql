-- Снижение бесплатных бустов по подписке, чтобы не засорять ленту.

UPDATE public.subscription_plans
SET
  boosts_per_day = CASE tier_key
    WHEN 'creator' THEN 1
    WHEN 'pro' THEN 2
    WHEN 'label' THEN 3
    ELSE boosts_per_day
  END,
  boost_duration_hours = CASE tier_key
    WHEN 'creator' THEN 1
    WHEN 'pro' THEN 1
    WHEN 'label' THEN 24
    ELSE boost_duration_hours
  END,
  features = CASE tier_key
    WHEN 'creator' THEN '["20 треков/месяц бесплатно","Сверх лимита — 20₽/трек","Буст 1×/день на 1 час","Бейдж Creator","Участие в конкурсах","Маркетплейс (продажа)","1 Blockchain-сертификат/мес","Приоритетная модерация (~3 дня)"]'::jsonb
    WHEN 'pro' THEN '["30 треков/месяц бесплатно","Сверх лимита — 20₽/трек","Буст 2×/день по 1 часу","Бейдж PRO 🔥","Приоритет в конкурсах","Детальная аналитика","3 Blockchain-сертификата/мес","Ускоренная модерация (~24ч)","Радио: приоритет ×1.5 + 2 слота/нед","Скидка на радио-аукцион 20%","Статистика радио в ЛК","БЕЗ РЕКЛАМЫ везде"]'::jsonb
    WHEN 'label' THEN '["50 треков/месяц бесплатно","Сверх лимита — 20₽/трек","Буст 3×/день на 24 часа","Золотой бейдж LABEL 👑","Максимальный приоритет в конкурсах","Расширенная аналитика + экспорт","10 Blockchain-сертификатов/мес","Экспресс-модерация (~12ч)","Радио: приоритет ×2.5 + 5 слотов/нед","Скидка на радио-аукцион 50%","Расширенная статистика радио + API","БЕЗ РЕКЛАМЫ везде","Приоритетная поддержка"]'::jsonb
    ELSE features
  END
WHERE tier_key IN ('creator', 'pro', 'label');
