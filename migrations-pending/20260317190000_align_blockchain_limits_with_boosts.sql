-- Синхронизируем бесплатные blockchain-метки с количеством бесплатных бустов по подписке.

UPDATE public.subscription_plans
SET
  deposits_free_monthly = CASE tier_key
    WHEN 'creator' THEN 1
    WHEN 'pro' THEN 2
    WHEN 'label' THEN 3
    ELSE deposits_free_monthly
  END,
  features = CASE tier_key
    WHEN 'creator' THEN '["20 треков/месяц бесплатно","Сверх лимита — 20₽/трек","Буст 1×/день на 1 час","Бейдж Creator","Участие в конкурсах","Маркетплейс (продажа)","1 Blockchain-сертификат/мес","Приоритетная модерация (~3 дня)"]'::jsonb
    WHEN 'pro' THEN '["30 треков/месяц бесплатно","Сверх лимита — 20₽/трек","Буст 2×/день по 1 часу","Бейдж PRO 🔥","Приоритет в конкурсах","Детальная аналитика","2 Blockchain-сертификата/мес","Ускоренная модерация (~24ч)","Радио: приоритет ×1.5 + 2 слота/нед","Скидка на радио-аукцион 20%","Статистика радио в ЛК","БЕЗ РЕКЛАМЫ везде"]'::jsonb
    WHEN 'label' THEN '["50 треков/месяц бесплатно","Сверх лимита — 20₽/трек","Буст 3×/день на 24 часа","Золотой бейдж LABEL 👑","Максимальный приоритет в конкурсах","Расширенная аналитика + экспорт","3 Blockchain-сертификата/мес","Экспресс-модерация (~12ч)","Радио: приоритет ×2.5 + 5 слотов/нед","Скидка на радио-аукцион 50%","Расширенная статистика радио + API","БЕЗ РЕКЛАМЫ везде","Приоритетная поддержка"]'::jsonb
    ELSE features
  END
WHERE tier_key IN ('creator', 'pro', 'label');
